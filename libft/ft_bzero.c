#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	int		i;
	char	*str;

	str = (char*)s;
	i = 0;
	while (i < (int)n)
	{
		str[i] = 0;
		i++;
	}
	s = (void*)str;
}
