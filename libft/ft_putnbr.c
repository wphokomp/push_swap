#include "libft.h"

void	ft_putnbr(int n)
{
	if (n > 2147483647 || n < -2147483647)
		return ;
	if (n == -2147483647)
	{
		ft_putstr("-2147483647");
		return ;
	}
	if (n < 0)
	{
		ft_putchar('-');
		n *= -1;
	}
	if (n >= 10)
	{
		ft_putnbr(n / 10);
		ft_putnbr(n % 10);
	}
	else
		ft_putchar(n + '0');
}
